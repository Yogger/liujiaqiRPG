package rpg.pojo;

/**任务类
 * @author ljq
 *
 */
public class Task {
	private int id;
	private String name;
	private int reqid;
	private int num;
	private int money;
	private int awardId;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the reqid
	 */
	public int getReqid() {
		return reqid;
	}
	/**
	 * @param reqid the reqid to set
	 */
	public void setReqid(int reqid) {
		this.reqid = reqid;
	}
	/**
	 * @return the num
	 */
	public int getNum() {
		return num;
	}
	/**
	 * @param num the num to set
	 */
	public void setNum(int num) {
		this.num = num;
	}
	/**
	 * @return the money
	 */
	public int getMoney() {
		return money;
	}
	/**
	 * @param money the money to set
	 */
	public void setMoney(int money) {
		this.money = money;
	}
	/**
	 * @return the awardId
	 */
	public int getAwardId() {
		return awardId;
	}
	/**
	 * @param awardId the awardId to set
	 */
	public void setAwardId(int awardId) {
		this.awardId = awardId;
	}
}
