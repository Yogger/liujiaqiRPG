package rpg.pojo;

/**等级类
 * @author ljq
 *
 */
public class Level {
	private int id;
	private int expl;
	private int expr;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the expl
	 */
	public int getExpl() {
		return expl;
	}
	/**
	 * @param expl the expl to set
	 */
	public void setExpl(int expl) {
		this.expl = expl;
	}
	/**
	 * @return the expr
	 */
	public int getExpr() {
		return expr;
	}
	/**
	 * @param expr the expr to set
	 */
	public void setExpr(int expr) {
		this.expr = expr;
	}
}
